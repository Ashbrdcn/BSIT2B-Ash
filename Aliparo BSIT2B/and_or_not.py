"""
Demonstrating the usage of logical operators: 'and', 'or', and 'not' in Python.
"""

# Defining boolean variables
a = True
b = False

# 'and' operator:
# - Returns True only if both operands are True, otherwise False.
print(f"a and b (True and False): {a and b}")  # Expected output: False

# 'or' operator:
# - Returns True if at least one operand is True.
print(f"a or b (True or False): {a or b}")  # Expected output: True

# 'not' operator:
# - Negates the boolean value.
print(f"not a (not True): {not a}")  # Expected output: False
print(f"not b (not False): {not b}")  # Expected output: True
